////////////////////////////////////////////////////////////////////////////
#define SKY_LIGHT_BRIGHTNESS 0.75f
////////////////////////////////////////////////////////////////////////////
vector3 CalculateAmbient(vector LightMapAO)
{
    return env_color.rgb * (pow(LightMapAO, 1.6h) * SKY_LIGHT_BRIGHTNESS) + L_ambient.rgb;
}
