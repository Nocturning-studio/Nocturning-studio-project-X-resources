// Average functions
float avg2(float2 tex)
{
    return (tex.x + tex.y) * 0.5f;
};

float avg3(float3 tex)
{
    return (tex.x + tex.y + tex.z) * 0.3333333f;
};
////////////////////////////////////////////////////////////////////////////
float avg4(float4 tex)
{
    return (tex.x + tex.y + tex.z + tex.w) * 0.25f;
};
