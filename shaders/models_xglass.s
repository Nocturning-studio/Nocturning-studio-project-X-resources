function l_special  (shader, t_base, t_second, t_detail)
  shader:begin	("model_distort4glass","particle_distort")
      : sorting   (1, true)
      : blend     (true,blend.srcalpha,blend.invsrcalpha)
      : zb        (true,false)
      : fog       (false)
      : distort   (true)
  shader:sampler  ("s_base")      :texture  (t_base): f_anisotropic() : srgb()
  shader:sampler  ("s_distort")   :texture  ("pfx\\pfx_dist_glass2")
end

function normal   (shader, t_base, t_second, t_detail)
  shader:begin    ("model_env_lq","model_env_lq")
      : fog       (true)
      : zb        (true,false)
      : blend     (true,blend.srcalpha,blend.invsrcalpha)
      : aref      (true,0)
      : sorting   (1,true)
      : distort   (true)
  shader:sampler  ("s_base")       :texture    (t_base): f_anisotropic() : srgb()
  shader:sampler  ("sky_s0")       :texture  ("$user$sky0"): srgb()
  shader:sampler  ("sky_s1")       :texture  ("$user$sky1"): srgb()
end
