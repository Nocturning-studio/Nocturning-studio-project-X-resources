function normal(shader, t_base, t_second, t_detail)
	shader:begin ("detail_wave","detail")
	: blend (false,blend.one,blend.one)
	: zb (true,true)
	: aref(true, 128)
	: fog (false)
	: sampler("s_base") : texture (t_base) : clamp()
	shader:sampler ("s_lmap") : texture ("internal\\internal_light_att")
	: clamp ()
	: f_linear ()
	: project (true)
	shader:sampler ("s_att") : texture ("internal\\internal_light_attclip")
	: clamp ()
	: f_linear ()
end