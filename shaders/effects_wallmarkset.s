function normal		(shader, t_base, t_second, t_detail)
	shader:begin	("wmark",	"wmark")
			: sorting	(1, false)
			: aref 		(false,0)
			: zb 		(true,true)
			: fog		(false)
			: wmark		(true)
	shader:sampler	("s_base")      :texture	(t_base): f_anisotropic() : srgb()
	shader:sampler  ("s_gbuffer_2") :texture  ("$user$gbuffer_2")
end
