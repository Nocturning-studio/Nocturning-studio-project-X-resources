struct LightMapData
{
    float AO;
    float Sun;
};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
LightMapData Unpack_LightMap(float2 LightMapUV)
{
    LightMapData Output;

    float4 LightmapData = tex2D(s_hemi, LightMapUV);

    Output.AO = avg3(LightmapData.rgb) * AO_BRIGHTNESS;
    Output.Sun = LightmapData.a;

    return Output;
}
